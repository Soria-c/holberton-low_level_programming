/**
 * op_add - sums a and b.
 * @a: integer.
 * @b: integer.
 * Return: sum of a between b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts b from a.
 * @a: integer.
 * @b: integer.
 * Return: subtraction of b from a.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a by b.
 * @a: integer.
 * @b: integer.
 * Return: product of a between b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a by b.
 * @a: integer.
 * @b: integer.
 * Return: division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  takes modulo b from a.
 * @a: integer.
 * @b: integer.
 * Return: modulo b of a.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
